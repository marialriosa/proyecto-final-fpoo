    private void crearJListVentas(){
        for (var item : Restaurante.getVentas()) {
            listModelVentas.addElement(item.getCodigo());
        }
        this.listaVentas.setModel(listModelVentas);
    }
   
    private void eliminarVenta(){
        Restaurante.getVentas().remove(listaVentas.getSelectedIndex());
        listModelVentas.remove(listaVentas.getSelectedIndex());
        
    }
