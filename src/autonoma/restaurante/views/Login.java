    private void iniciarSesion(){
        if(Restaurante.getAdministrador().getNombreUsuario().equals(this.usuario.getText()) 
                && Restaurante.getAdministrador().getContrasena().equals(new String(this.contrasena.getPassword()))){
            this.usuarioNoEncontrado.setVisible(false);
            this.setVisible(false);
            AccesoModulosVista ventanaAccesoModulos = new AccesoModulosVista(this, true);
            ventanaAccesoModulos.setVisible(true);
            
        } else {
            this.usuarioNoEncontrado.setVisible(true);
        }
    }
