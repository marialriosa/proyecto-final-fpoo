    private void generarEstadosFinancieros(){
        Double totalGanancias = 0d;
        Double totalVentas = 0d;
        for(var venta : Restaurante.getVentas()){
            totalGanancias += venta.getValorGanancia();
            totalVentas += venta.getValorTotal();
        }
        
        this.fechaCreacion.setText(new Date().toString());
        this.totalGanancias.setText(Formateador.formatearDouble(totalGanancias));
        this.totalVentas.setText(Formateador.formatearDouble(totalVentas));
        
        var estadoFinanciero = new EstadoFinanciero(new Date(), totalVentas, totalGanancias);
        Restaurante.estadoFinanciero.add(estadoFinanciero);
        this.listModelEstadoFinanciero.addElement(estadoFinanciero.getFechaGeneracion().toString());
    }

    private void mostrarEstadoFinanciero(){
        var estadoFinanciero = Restaurante.estadoFinanciero.get(this.listaEstadosFinancieros.getSelectedIndex());
        this.fechaCreacion.setText(estadoFinanciero.getFechaGeneracion().toString());
        this.totalGanancias.setText(Formateador.formatearDouble(estadoFinanciero.getTotalGanancias()));
        this.totalVentas.setText(Formateador.formatearDouble(estadoFinanciero.getValorTotalRecaudado()));
    }
